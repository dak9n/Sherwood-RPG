/**
 * Решение монстра при получении урона: вздрогнуть или устоять.
 *
 * Вынесено из monster.ts по той же причине, что и decideChase: тот тянет Phaser
 * и в тестах не грузится, а ошибка была ровно логическая.
 *
 * ЧТО БЫЛО СЛОМАНО. Найдено заказчиком на живой игре: «когда я быстро бью
 * босса, он не успевает меня ударить». На деле — не «не успевает», а не может
 * в принципе. takeDamage сбрасывал состояние в 'hurt' от ЛЮБОГО удара, в том
 * числе посреди замаха, а update() в состоянии 'hurt' выходит сразу. Считаем:
 *
 *   взмах игрока      8 кадров / 16 к/с = 500 мс (удар на 4-м кадре)
 *   «вздрогнул»       4 кадра  / 12 к/с = 333 мс
 *   замах голема      9 кадров / 16 к/с = 562 мс
 *
 * Голему нужно 562 мс без помех, а урон прилетает каждые 500. Замах сбивался
 * всегда, на любом боссе, при простом удержании кнопки. У грибов это незаметно:
 * они умирают за девять взмахов, и заметить нечего. Босс на 1500 hp становился
 * неподвижной грушей — весь бой обесценивался.
 *
 * КАК ЧИНИМ. Два независимых правила, и нужны оба.
 *
 * 1. ЗАМАХ НЕ СБИТЬ (steadyAttack). Начал бить — ударит. Это то, что в играх
 *    зовут super armor, и без него любой крупный враг обречён: чем больше у
 *    него кадров в замахе, тем легче его залочить.
 *
 * 2. ВЗДРАГИВАЕТ НЕ ЧАЩЕ РАЗА В flinchMs. Одного правила (1) не хватает: между
 *    замахами монстр ждёт отката, и если каждый удар роняет его в 'hurt', он не
 *    успевает даже НАЧАТЬ следующий замах. При взмахе раз в 500 мс и вздрагивании
 *    на 333 свободного окна остаётся 167 мс — впритык, и это ещё до дождя стрел,
 *    который бьёт очередью и закрывает окно совсем.
 *
 * Урон при этом засчитывается ВСЕГДА — гасится только реакция. Белая вспышка на
 * спрайте остаётся в monster.ts вне этой развилки: игрок обязан видеть, что удар
 * прошёл, даже когда босс на него не повёл плечом.
 *
 * Грибов это не касается: без flinchMs и steadyAttack поведение прежнее —
 * вздрагивают от каждого удара. Прерывание боя уроном для них награда за то,
 * что ударил первым, и отбирать её не за чем.
 */

export type Flinch = 'flinch' | 'steady';

export interface FlinchStats {
  /**
   * Замах не сбивается уроном. Без этого крупный враг тем беспомощнее, чем
   * длиннее у него анимация удара.
   */
  steadyAttack?: boolean;
  /**
   * Не вздрагивать чаще, чем раз в столько мс. Без указания (и 0) — вздрагивать
   * от каждого удара, как было.
   */
  flinchMs?: number;
}

export interface FlinchInput {
  /** Монстр сейчас в замахе. */
  attacking: boolean;
  now: number;
  /** Раньше этого момента вздрагивать нельзя. Ставится прошлым вздрагиванием. */
  nextFlinchAt: number;
}

export function decideFlinch(input: FlinchInput, stats: FlinchStats): Flinch {
  // Замах главнее отката: даже если вздрогнуть уже можно, начатый удар доводим.
  if (stats.steadyAttack && input.attacking) return 'steady';

  if (stats.flinchMs && input.now < input.nextFlinchAt) return 'steady';

  return 'flinch';
}
