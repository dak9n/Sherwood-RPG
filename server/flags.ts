/**
 * Что включено на этом сервере.
 *
 * Единственный флаг сейчас — рынок, и он выключен не из осторожности вообще,
 * а по конкретной причине. Рынок написан так, что сервер верит клиенту:
 *
 *   - market.list() регистрирует лот, ничего не проверяя. Комментарий в
 *     server/market-store.ts прямо говорит: «Предмет игрок уже списал у себя»;
 *   - при покупке сервер сообщает цену, а золото списывает клиент
 *     (src/scenes/GameScene.ts);
 *   - /__save-progress кладёт присланный сейв как есть, без разбора.
 *
 * Пока всё это жило под `apply: 'serve'`, обманывать было некого: сервер и
 * жулик — один человек за одним компьютером. Ровно эта строчка и была защитой.
 * Отдельный процесс её снимает: цепочка «выставить лот -> отменить -> забрать
 * из почты -> продать в лавке» печатает предметы и золото парой запросов, и
 * напечатанное расходится честным игрокам через тот же рынок.
 *
 * Поэтому рынок остаётся выключенным до того этапа, на котором инвентарь и
 * золото станут серверными. Выключен он именно на сервере, а не спрятанной
 * кнопкой в интерфейсе: прятать кнопку от того, кто умеет открыть консоль, —
 * не защита.
 */

/**
 * Рынок отвечает 503 вместо того, чтобы работать.
 *
 * ENABLE_MARKET=1 включает его обратно — это для отладки самого рынка на своей
 * машине, а не для боевого сервера.
 */
export const ENABLE_MARKET = process.env.ENABLE_MARKET === '1';

